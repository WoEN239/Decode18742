package barrel.enumerators;

public abstract class Template
{
    public enum Name { NOT_IMPLEMENTED }

    Name memName;
    int memId;


    public void Set(int value)
    {
        memId = value;
        memName = ToName(value);
    }
    public void Set(Name name)
    {
        memId = ToInt(name);
        memName = name;
    }
    public void Set(int value, Name name)
    {
        memId = value;
        memName = name;
    }
    public void Set(Name name, int value)
    {
        memId = value;
        memName = name;
    }


    public Name GetName()
    {
        return memName;
    }
    public int GetId()
    {
        return memId;
    }



    public Name ToName (int value)
    {
        return Name.NOT_IMPLEMENTED;
    }
    public int ToInt (Name name)
    {
        return -1;
    }
}
